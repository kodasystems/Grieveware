#include <iostream> // Header file library - lets us work with inputs/outputs. Adds functionality
using namespace std; // allows us to use names for objects and variables from the library 
// white space is ignored

/*
DICTIONARY
--------------
#include <iostream> // Header file library - lets us work with inputs/outputs. Adds functionality
using namespace std; // allows us to use names for objects and variables from the library 

cout (object) << (insertion operator) - output/print text | \n (escape sequence) - goes to next line
\n\n - empty line
 endl - break line
return - ends function

\t - create horizontal tab
\\ - insert backslash
\" - insert double quotes
*/

// function
int main() {
  cout << "Hello World! \n"; // cout (object) << (insertion operator) - output/print text | \n (escape sequence) - goes to next line
  cout << "I am learning C++" << "\n\n"; // \n\n - empty line
  cout << "I am learning!" << endl; // endl - break line
  return 0; // ends function
}